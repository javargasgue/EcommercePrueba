package com.mycompany.ecommerce;
/**
 *
 * @author Lenovo
 */
public class Clase_Categoria {
    
    private String nombre ;
    private String foto ;
    private String categoria_Padre ;
    
    public void categoria(){
    }
    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }
    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    /**
     * @return the foto
     */
    public String getFoto() {
        return foto;
    }
    /**
     * @param foto the foto to set
     */
    public void setFoto(String foto) {
        this.foto = foto;
    }
    /**
     * @return the categoria_Padre
     */
    public String getCategoria_Padre() {
        return categoria_Padre;
    }
    /**
     * @param categoria_Padre the categoria_Padre to set
     */
    public void setCategoria_Padre(String categoria_Padre) {
        this.categoria_Padre = categoria_Padre;
    }
}
